import cv2
import torch
import numpy as np


__all__ = ["draw_bboxes", "draw_mask"]


def draw_bboxes(img, bboxes, categories, scores, category_names):
    if isinstance(img, str):
        img = cv2.imread(img)
    if isinstance(bboxes, torch.Tensor):
        bboxes = bboxes.detach().cpu().numpy()
    if isinstance(categories, torch.Tensor):
        categories = categories.detach().cpu().numpy()
    if isinstance(scores, torch.Tensor):
        scores = scores.detach().cpu().numpy()

    for bbox, category_id, score in zip(bboxes, categories, scores):
        category_name = category_names[category_id]
        x0 = int(bbox[0])
        y0 = int(bbox[1])
        x1 = int(bbox[2])
        y1 = int(bbox[3])

        color = (_COLORS[category_id] * 255).astype(np.uint8).tolist()
        text = '{}:{:.1f}%'.format(category_name, score * 100)
        txt_color = (0, 0, 0) if np.mean(_COLORS[category_id]) > 0.5 else (255, 255, 255)
        font = cv2.FONT_HERSHEY_SIMPLEX

        txt_size = cv2.getTextSize(text, font, 0.4, 1)[0]
        cv2.rectangle(img, (x0, y0), (x1, y1), color, 2)

        txt_bk_color = (_COLORS[category_id] * 255 * 0.7).astype(np.uint8).tolist()
        cv2.rectangle(
            img,
            (x0, y0 + 1),
            (x0 + txt_size[0] + 1, y0 + int(1.5*txt_size[1])),
            txt_bk_color,
            -1
        )
        cv2.putText(img, text, (x0, y0 + txt_size[1]), font, 0.4, txt_color, thickness=1)
    
    return img


def draw_mask(img, mask):
    if isinstance(img, str):
        img = cv2.imread(img)

    imH, imW = img.shape[:2]
    if mask.shape != img.shape:
        mask = cv2.resize(mask, (imW, imH))
    color_mask = cv2.applyColorMap(mask, cv2.COLORMAP_SUMMER)

    img_mask = np.zeros((2 * imH, imW * 2 + 30, 3))
    img_mask[:imH, :imW, :] = img
    img_mask[:imH, (imW + 30):, :] = color_mask
    fusion_img = 0.3 * img + 0.7 * color_mask
    fusion_img = fusion_img.astype(np.uint8)
    img_mask[imH:, (imW + 15 - imW // 2):(2 * imW + 15 - imW // 2)] = fusion_img
    return img_mask


